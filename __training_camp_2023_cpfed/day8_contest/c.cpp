int phi (int n) {
	int result = n;
	for (int i=2; i*i<=n; ++i)
		if (n % i == 0) {
			while (n % i == 0)
				n /= i;
			result -= result / i;
		}
	if (n > 1)
		result -= result / n;
	return result;
}


void solve() {
    
    int m;
    cin >> m;

    cout << 1LL * phi(m) * (m - 1) << "\n";   

}


