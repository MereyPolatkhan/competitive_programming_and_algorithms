void solve() {
    string s;
    cin >> s;

    int n = 0, m = 0;
    for (int i = 0; i <= 2; i++) {
        n += (int)s[i] - '0';
    }
    for (int i = 3; i <= 5; i++) {
        m += (int)s[i] - '0';
    }

    if (n == m) 
        cout << "YES\n";
    else
        cout << "NO\n";

}

