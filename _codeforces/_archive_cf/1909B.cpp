void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    int p = 1;
    while (1) {
        for (int i = 1; i < n; i++) {
            if (a[0] % (int)pow(2, p) != a[i] % (int)pow(2, p)) {
                cout << (int)pow(2, p) << "\n";
                return;
            }
        }
        p++;
    }

}

