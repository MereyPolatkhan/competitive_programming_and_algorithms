#define rall(x) x.rbegin(), x.rend()
  
typedef long long ll;
typedef unsigned long long ull;
typedef pair<int, int> pii;
typedef unsigned short int usi;


void solve() {
    int n; ll x;
    cin >> n >> x;

    vector<ll> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    sort(rall(a));

    int res = 0;
    ll cnt = 0;
    for (int r = 0; r < n; r++) {
        cnt++;
        if (cnt * a[r] >= x) {
            res++;
            cnt = 0;
        }
    }
    cout << res << "\n";

    cout << "\n\n\n";
}

