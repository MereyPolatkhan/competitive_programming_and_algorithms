ll func(ll sum, ll cnt_odd) {
    return sum - cnt_odd / 3 - (cnt_odd % 3) % 2;
}


void solve() {
    int n;
    cin >> n;
    
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    ll sum = 0, cnt = 0;
    for (int &x: a) {
        sum += x;
        if (x % 2) 
            cnt++;

        if (sum == x) {
            cout << sum << " ";
        } 
        else {
            cout << func(sum, cnt) << " ";
        }
    }
    cout << "\n";
}

