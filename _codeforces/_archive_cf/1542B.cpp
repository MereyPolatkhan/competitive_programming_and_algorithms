void solve() {
    ll n, a, b;
    cin >> n >> a >> b;

    bool flag = false;
    if (a == 1) {
        flag = ((n - 1) % b == 0);
    }
    else {
        ll p = 1;
        while (p <= n) {
            if ((n - p) % b) 
                p *= a;
            else {
                flag = true;
                break;
            }
        }
    }
    if (flag) 
        cout << "YES\n";
    else 
        cout << "NO\n";
}


