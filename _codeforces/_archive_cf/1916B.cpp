void solve() {
    ll a, b;
    cin >> a >> b;

    ll g = gcd(a, b);
    ll lca = a * b / g;
    if (a == 1)
        lca = b * b;
    else if (lca == b) {
        for (ll i = 2; i * i <= b; i++) {
            if (b % i == 0) {
                lca *= i;
                break;
            }
        }
    }

    cout << lca << "\n";
}

