void solve() {
    int n;
    cin >> n;

    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    vector<int> b = a;
    sort(all(b));

    multiset<int> st;
    int i = 0;
    while (a[i] != b[0]) {
        st.insert(a[i]);
        i++;
    }
    int j = 0;
    int res = st.size();

    while (i < n and j < n) {
        if (a[i] == b[j]) {
            i++;
            j++;
        }
        else {
            if (st.count(b[j])) {
                st.erase(st.find(b[j]));
                j++;
            }
            else {
                res = -1;
                break;
            }
        }
    }

    cout << res << "\n";
}



