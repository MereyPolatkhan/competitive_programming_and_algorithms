void solve() {
    string s;
    cin >> s;

    int cur = 1;

    int res = 0;
    for (char c: s) {
        int x = (int)c - (int)'0';

        if (x == 0) {
            x = 10; 
        }
        res += abs(cur - x) + 1;
        cur = x;
    }
    cout << res << '\n';

}

