#include <bits/stdc++.h>

using namespace std;

#define pb push_back
#define all(x) x.begin(), x.end()
#define rall(x) x.rbegin(), x.rend()
  
typedef long long ll;
typedef unsigned long long ull;
typedef pair<int, int> pii;


void solve() {
    int n;
    cin >> n;

    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }

    ll res = 0;

    vector<int> indexes;
    for (int i = 1; i <= n; i++) {
        if (a[i] >= i) {
            continue;
        }        
        res += lower_bound(all(indexes), a[i]) - indexes.begin();
        indexes.pb(i);
    }
    cout << res << "\n";

}       


